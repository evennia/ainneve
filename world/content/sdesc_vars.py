def with_articles(wordlist):
    """Prepends the appropriate article to a list of words"""
    output = []
    vowels = 'aeiou'
    for word in wordlist:
        if word[0] in vowels:
            output.append("an {}".format(word))
        else:
            output.append("a {}".format(word))

    return output


